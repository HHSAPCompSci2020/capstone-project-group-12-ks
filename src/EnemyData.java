/**
 * Holds data for the enemy in the database
 * @author kinjal
 *
 */
public class EnemyData{
	
	EnemyData(){
		
	}
	public double x, y;
	
	/**
	 * getter for x coord
	 * @return x the x coord of the enemy
	 */
	public double getX(){
		return x;
	}
	/**
	 * getter for y coord
	 * @return y the y coord of the enemy
	 */
	public double getY(){
		return y;
	}
}